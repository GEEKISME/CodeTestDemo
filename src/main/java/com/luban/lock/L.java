package com.luban.lock;

public class L {
    int  flag = 5;
    boolean fl = true;

    public L(int flag, boolean fl) {
        this.flag = flag;
        this.fl = fl;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }

    public boolean isFl() {
        return fl;
    }

    public void setFl(boolean fl) {
        this.fl = fl;
    }
}
